$(document).ready( function() {
	$("#jquery").click(function(event) {
		alert("Thank you for the visiting!");
		event.preventDefault();
		$(this).removeClass("test");
	});
	
	$("#show").click(function(){
		$("#jquery").show("slow");
		$("#jquery").addClass("test");
	});
	
	$("#hide").click(function(){
		$(".test").removeClass("test");
		$("#jquery").hide("slow");
	});
	
});

